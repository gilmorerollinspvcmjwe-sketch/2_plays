// 获取随机角色评论
export function getRandomCharacterComment(type?: keyof typeof characterCommentTemplates): string {
  const types = Object.keys(characterCommentTemplates) as Array<keyof typeof characterCommentTemplates>;
  const selectedType = type || types[Math.floor(Math.random() * types.length)];
  const templates = characterCommentTemplates[selectedType];
  return templates[Math.floor(Math.random() * templates.length)];
}

// 根据玩家类型获取角色评论
export function getCharacterCommentByPlayerType(playerType: '氪金大佬' | '剧情党' | '外观党' | '休闲玩家'): string {
  switch (playerType) {
    case '外观党':
      return getRandomCharacterComment('appearance');
    case '剧情党':
      return getRandomCharacterComment(Math.random() > 0.5 ? 'story' : 'personality');
    case '氪金大佬':
      return getRandomCharacterComment('strength');
    default:
      return getRandomCharacterComment();
  }
}
